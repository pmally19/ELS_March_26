const router = express.Router();

// Get all addresses for a customer
router.get('/:customerId/addresses', async (req, res) => {
  try {
    const { customerId } = req.params;

    const query = `
      SELECT 
        id, address_number, customer_id, address_type, address_name, contact_person, company_name,
        address_line_1, address_line_2, city, state, country, postal_code, region,
        phone, email, is_primary, is_active, notes, created_at, updated_at
      FROM customer_addresses 
      WHERE customer_id = $1 AND is_active = true
      ORDER BY address_type, is_primary DESC, address_name
    `;

    const result = await pool.query(query, [customerId]);

    res.json({
      success: true,
      data: result.rows
    });

  } catch (error) {
    console.error('Error fetching customer addresses:', error);
    res.status(500).json({
      success: false,
      error: 'Failed to fetch customer addresses'
    });
  }
});

