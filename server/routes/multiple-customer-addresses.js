// Get address summary for all customers
router.get('/address-summary', async (req, res) => {
  try {
    const query = `
      SELECT 
        c.id, c.name, c.customer_code,
        COUNT(DISTINCT ca.id) as total_addresses,
        COUNT(DISTINCT CASE WHEN ca.address_type = 'sold_to' THEN ca.id END) as sold_to_count,
        COUNT(DISTINCT CASE WHEN ca.address_type = 'bill_to' THEN ca.id END) as bill_to_count,
        COUNT(DISTINCT CASE WHEN ca.address_type = 'ship_to' THEN ca.id END) as ship_to_count,
        COUNT(DISTINCT CASE WHEN ca.address_type = 'payer_to' THEN ca.id END) as payer_to_count,
        COUNT(DISTINCT car.id) as relationship_count
      FROM erp_customers c
      LEFT JOIN customer_addresses ca ON c.id = ca.customer_id AND ca.is_active = true
      LEFT JOIN customer_address_relationships car ON c.id = car.customer_id AND car.is_active = true
      WHERE c.active = true
      GROUP BY c.id, c.name, c.customer_code
      ORDER BY c.name
    `;

    const result = await pool.query(query);

    res.json({
      success: true,
      data: result.rows
    });

  } catch (error) {
    console.error('Error fetching address summary:', error);
    res.status(500).json({
      success: false,
      error: 'Failed to fetch address summary'
    });
  }
});

